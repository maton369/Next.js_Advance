import Link from "next/link";
import styles from "./style.module.css";

/**
 * Nav（ナビゲーションコンポーネント）
 *
 * - Next.js の <Link> を使ってページ遷移リンクを提供するサイド/ヘッダーナビ想定の部品。
 * - RootLayout などの共通レイアウトに配置することで、全ページ共通のナビとして機能する。
 *
 * アルゴリズム的な見方（UIの動き方）：
 * - 画面には「リンクの集合（メニュー）」を描画する。
 * - ユーザーがリンクをクリックすると、Next.js のルーターが href を解決し、
 *   クライアントサイド遷移（ページ全体の再読み込みを避ける遷移）を行う。
 * - その結果、RootLayout の枠（Header/Nav/Footer）は維持されつつ、`children`（ページ本体）だけが差し替わる。
 */
export function Nav() {
  return (
    /**
     * <nav> は HTML のセマンティック要素で、「ナビゲーション領域」であることを示す。
     * - スクリーンリーダー等の支援技術が、ページ内の主要な移動手段として認識しやすくなる。
     *
     * styles.nav は CSS Modules で定義されたクラス。
     * - CSS Modules によりクラス名がローカルスコープ化され、他のコンポーネントの style と衝突しにくい。
     * - ここではレイアウト（幅、余白、背景、固定表示など）を nav 専用に調整する意図がある。
     */
    <nav className={styles.nav}>
      {/**
       * <ul> / <li> を使うのは「メニューはリスト構造」という意味付けができるため。
       * - 見た目だけでなく、構造としても自然なのでアクセシビリティ面でも相性が良い。
       */}
      <ul>
        <li>
          {/**
           * Next.js の Link コンポーネント
           *
           * - href="/" はトップページ（ルート）への遷移を表す。
           * - <a> を直接使う代わりに Link を使うことで、Next.js がルーティングを最適化し、
           *   可能であればクライアントサイド遷移（SPA的な遷移）になる。
           *
           * 挙動のポイント：
           * - クリック時にブラウザのフルリロードを避けられるため、体感が速くなりやすい。
           * - App Router 構成だと、共通レイアウト（RootLayout）が維持されやすく、
           *   中身（children）だけが切り替わる動きになる。
           */}
          <Link href="/">トップ</Link>
        </li>

        <li>
          {/**
           * href="/categories" は /categories ルートへの遷移。
           * - `app/categories/page.tsx` が存在すればそこが表示される（App Router のファイルルーティング）。
           * - まだページが無い場合は Not Found になり得るので、ルート側の実装とセットで管理するのが重要。
           */}
          <Link href="/categories">カテゴリー一覧</Link>
        </li>
      </ul>
    </nav>
  );
}